//arithmetic operator -----> math vala operator
//binary operator(work on two operands)
let a= 5;
let b= 2;

console.log("a+b =",a+b);
console.log("a-b =",a-b);
console.log("a*b =",a*b);
console.log("a/b =",a/b);
console.log("a%b =",a%b);        // modulus/modulo , a , b se divide hoga toh jo remainder aayega vhi hoga
console.log("a**b =",a**b);      //exponentiation , a^b
 
//unary operator ( work on on operand)
 a++;  // this means a=a+1  // it is  called post increment
 b--; // this means b=b-1   // it is called post decrement
console.log("a= ",a);
console.log("b = ",b);

//pre increment and pre decrement
let x= 5;
let y =3;
--x;
++y;
console.log('x = ',x);
console.log('y = ',y);

//when printing is done in only one line
let c = 6;
let d= 2;
console.log("c = ", ++c);  // pehle inc then print
console.log('d = ',d++);  // pehle print then inc



